#[derive(Clone)]
pub struct Token {
    token_type: TokenType,
    lexme: String,
    literal: (), // TODO
    line: usize,
}

impl Display for Token {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{} {} {}", self.token_type, self.lexme, "TODO: Literal")
    }
}

impl Token {
    pub fn new(token_type: TokenType, lexme: String, literal: (), line: usize) -> Self {
        Self {
            token_type,
            lexme,
            line,
            literal,
        }
    }
}

pub struct Scanner {
    source: String,
    start: usize,
    current: usize,
    line: usize,
    tokens: Vec<Token>,
}

impl Scanner {
    pub fn new(source: String) -> Self {
        Self {
            source,
            start: 0,
            current: 0,
            line: 1,
            tokens: vec![],
        }
    }

    pub fn scan_tokens(&mut self) -> Vec<Token> {
        while !self.is_at_end() {
            self.start = self.current;
            self.scan_token();
        }

        self.tokens
            .push(Token::new(TokenType::EOF, String::new(), (), self.line));
        self.tokens.clone()
    }

    fn is_at_end(&self) -> bool {
        return self.current >= self.source.len();
    }

    fn scan_token(&mut self) {
        match self.advance() {
            '(' => self.add_token(TokenType::LeftParen, None),
            ')' => self.add_token(TokenType::RightParen, None),
            '{' => self.add_token(TokenType::LeftBrace, None),
            '}' => self.add_token(TokenType::RightBrace, None),
            ',' => self.add_token(TokenType::Comma, None),
            '.' => self.add_token(TokenType::Dot, None),
            '-' => self.add_token(TokenType::Minus, None),
            '+' => self.add_token(TokenType::Plus, None),
            ';' => self.add_token(TokenType::Semicolon, None),
            '*' => self.add_token(TokenType::Star, None),
            invalid_char => lox_error(self.line, format!("Unexpected character: {invalid_char}")),
        }
    }

    fn advance(&mut self) -> char {
        let ch = self.source.chars().nth(self.current).unwrap();
        self.current += 1;
        ch
    }

    fn add_token(&mut self, token_type: TokenType, literal: Option<()>) {
        let text = self.source[self.start..=self.current].to_string();
        if let Some(v) = literal {
            self.tokens.push(Token::new(token_type, text, v, self.line));
        } else {
            self.tokens
                .push(Token::new(token_type, text, (), self.line));
        }
    }
}
